#!/bin/sh

# =================================================================
#		cat——查看文件内容
# =================================================================
# 在 UNIX 和 Linux 中，它有以下几种用途

# 把文本文件显示在屏幕上
# 复制文本文件
# 合并文本文件
# 创建新的文本文件
# -----------------------------------------------------------------
# 用法: cat [-nb] [FILE]...
#
# 选项参数：
#        -n      输出行号
#        -b      不包含空行
# -----------------------------------------------------------------




# =================================================================
#	 示例1：输出行号
# =================================================================
cat -b file_cat.sh




# =================================================================
#	 示例1：把文本文件显示在屏幕上
# =================================================================
cat a.txt

# output:
#-------------------------------------------------------------------
# 11111




# =================================================================
#	 示例2：把多个文件内容输出到屏幕
# =================================================================
cat a.txt b.txt

# output:
#-------------------------------------------------------------------
# 1111
# 2222




# =================================================================
#	 示例3：把多个文件内容输出到屏幕合并到一个文件
# =================================================================
cat a.txt b.txt > c.txt
cat c.txt

# output:
#-------------------------------------------------------------------
# 1111
# 2222




# =================================================================
#	 示例4：复制文件内容都新文件
# =================================================================
cat src.txt > dst.txt

# output:
#-------------------------------------------------------------------
# 1111




# =================================================================
#	 示例5：创建新的文本文件，复制文件
# =================================================================
cat < dst.txt > dst2.txt #copy dst to dst2

# output:
#-------------------------------------------------------------------
# 1111