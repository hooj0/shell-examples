#!/bin/sh

# =================================================================
#		sort —— 对字符串进行排序
# =================================================================
# 
# -----------------------------------------------------------------




# -----------------------------------------------------------------
#   准备工作
# -----------------------------------------------------------------
cat << EOF > example.txt
f
b
c
g
a
e
d
EOF



# =================================================================
#	 示例1：对 example.txt 中的行进行排序
# =================================================================
sort example.txt


# output:
#-------------------------------------------------------------------
#a
#b
#c
#d
#e
#f
#g


# =================================================================
#	 示例2：随机打乱已经排好序的 example.txt
# =================================================================
sort example.txt | sort -R

# output:
#-------------------------------------------------------------------
#d
#b
#a
#g
#e
#c
#f







read exits