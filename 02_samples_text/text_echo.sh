#!/bin/sh

# =================================================================
#		echo —— 输出命令之后的文本到标准输出或文件
# =================================================================
# 
# -----------------------------------------------------------------




# =================================================================
#	 示例1：
# =================================================================
echo Hello World

# output:
#-------------------------------------------------------------------
# Hello World


# =================================================================
#	 示例2：输出 "Hello World"，单词间用换行符分隔
# =================================================================
echo -ne "Hello\nWorld\n"


# output:
#-------------------------------------------------------------------
#Hello
#World



# =================================================================
#	 示例：输出当前目录文件名称，* 相当于是路径名展开
# =================================================================
echo *

# 输出 txt 结尾文件名
echo *.txt
# 输出 ls 开头文件名
echo ls*

# 使用通配符表达式
echo [[:upper:]]*

# 输出目录组成
echo /usr/*/share


# output:
#-------------------------------------------------------------------
#dir1 dir2 example.txt install-options.txt lazy-file.txt ls-error.txt ls-file.txt ls-output.txt test-sym2



read exits