#!/bin/sh

# =================================================================
#		tr —— 转换或删除字符
# =================================================================
# 
# -----------------------------------------------------------------




# -----------------------------------------------------------------
#   准备工作
# -----------------------------------------------------------------
cat << EOF > example.txt
Hello World Foo Bar Baz!
EOF



# =================================================================
#	 示例1：把所有小写字母转换成大写字母
# =================================================================
cat example.txt | tr 'a-z' 'A-Z'


# output:
#-------------------------------------------------------------------
# HELLO WORLD FOO BAR BAZ!




# =================================================================
#	 示例2：把所有的空格都转换为换行符
# =================================================================
cat example.txt | tr ' ' '\n'


# output:
#-------------------------------------------------------------------
#Hello
#World
#Foo
#Bar
#Baz!







read exits