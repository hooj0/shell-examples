# 
# -----------------------------------------------------------------




# =================================================================
#	 示例1：用空格分隔字符串，并提取
# =================================================================
echo "red riding hood went to the park to play" > example.txt

# 用空格分隔每一行，并输出第2，7，9列
cut -d " " -f 2,7,9 example.txt

# output:
#-------------------------------------------------------------------
# riding park play


