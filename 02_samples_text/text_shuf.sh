# =================================================================
#		shuf —— 随机选取文件的行输出
# =================================================================
# 
# -----------------------------------------------------------------


# =================================================================
#	 示例：准备数据
# =================================================================
cat << EOF > example.txt
1111
2222
3333
4444
EOF

# =================================================================
# 示例：随机从文件中获取数据输出
# =================================================================
shuf example.txt

# output:
#-------------------------------------------------------------------
#2222
#3333
#4444
#1111


# =================================================================
# 示例：随机输出数字
# =================================================================
# -e, --echo
shuf -e 3 5 6 7

# -i, --input-range=LO-HI
shuf -i 100-200

# output:
#-------------------------------------------------------------------
#







read exits