#!/bin/sh

# =================================================================
#		shuf —— 随机选取文件的行输出
# =================================================================
# 
# -----------------------------------------------------------------


# =================================================================
#	 示例：准备数据
# =================================================================
cat << EOF > example.txt
1111
2222
3333
4444
EOF

# =================================================================
#	 示例：
# =================================================================
shuf example.txt

# output:
#-------------------------------------------------------------------
#2222
#3333
#4444
#1111


# =================================================================
#	 示例：
# =================================================================


# output:
#-------------------------------------------------------------------
#







read exits