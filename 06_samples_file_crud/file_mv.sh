#!/bin/sh

# =================================================================
#		mv —— 移动文件
# =================================================================
# 把一个文件从一个位置移动到另一个位置
# -----------------------------------------------------------------
#   选项	                    意义
# -----------------------------------------------------------------
# -i --interactive	    在重写一个已经存在的文件之前，提示用户确认信息。 如果不指定这个选项，mv 命令会默认重写文件内容。
# -u --update	        当把文件从一个目录移动另一个目录时，只是移动不存在的文件， 或者文件内容新于目标目录相对应文件的内容。
# -v --verbose	        当操作 mv 命令时，显示翔实的操作信息。
# -----------------------------------------------------------------




# =================================================================
#	 示例1：移动文件，相当于重命名
# =================================================================
mv a.txt a.bak

# output:
#-------------------------------------------------------------------
# 


# =================================================================
#	 示例2：改变位置
# =================================================================
mv a.txt /home/a.txt

# output:
#-------------------------------------------------------------------
#







read exits