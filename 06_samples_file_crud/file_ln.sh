#!/bin/sh

# =================================================================
#		ln —— 创建文件连接
# =================================================================
# ln 命令既可创建硬链接，也可以创建符号链接。
# -----------------------------------------------------------------




# =================================================================
#	 示例：创建符号链接，它们和 Windows 的快捷方式差不多。
#         当文件a.txt修改，a_link 的内容也会变化。可以是一个文件或是一个目录
# 删除一个符号链接时，只有这个链接被删除
# =================================================================
ln a.txt a_link

# output:
#-------------------------------------------------------------------
# 




# =================================================================
#	 示例：创建硬链接
# =================================================================
ln -s item link

# output:
#-------------------------------------------------------------------
#







read exits