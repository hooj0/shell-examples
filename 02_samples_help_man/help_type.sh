# =================================================================
#       type —— 显示命令的类型
# =================================================================
# 命令的类型有以下几种：
#
#   可执行程序
#   内建于 shell 自身的命令
#   shell 函数
#   命令别名
# -----------------------------------------------------------------




# =================================================================
# 示例：查看命令类型
# =================================================================
type bash

# output:
#-------------------------------------------------------------------
# bash is /usr/bin/bash



# =================================================================
# 示例：查看命令类型
# =================================================================
type ls

# output:
#-------------------------------------------------------------------
# ls is hashed (/usr/bin/ls)



# =================================================================
# 示例：查看内建命令
# =================================================================
type type

# output:
#-------------------------------------------------------------------
# type is a shell builtin







