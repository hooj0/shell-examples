#!/bin/sh

# =================================================================
#		du —— 输出某些文件或目录的硬盘使用情况
# =================================================================
#   用法：du [option] [filename|directory]
#   选项:
#       -h (人类可读) 把结果以 KB、 MB 、GB 为单位输出。
#       -s (压缩总结) 输出一个目录总的磁盘空间占用情况，总结输出子目录的报告
# -----------------------------------------------------------------




# =================================================================
#	 示例1：统计目录空间大小
# =================================================================
du -sh /opt/

# output:
#-------------------------------------------------------------------
#18M	/opt/


# =================================================================
#	 示例2：统计目录下的所有文件空间大小
# =================================================================
du -sh /opt/*


# output:
#-------------------------------------------------------------------
#18M	/opt/VBoxGuestAdditions-5.1.26







read exits