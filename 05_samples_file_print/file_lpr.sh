# pr 程序用来把文本分页。
# 当打印文本的时候，经常希望用几个空行在输出的页面的顶部或底部添加空白。
# 此外，这些空行能够用来插入到每个页面的页眉或页脚。
# -----------------------------------------------------------------




# =================================================================
# 示例：打印显示分页：
#       pr 为 distros.txt 中的内容编订页码，用空行分开各页面，
#       生成了包含文件修改时间、文件名、页码的默认页眉。
# =================================================================
# -l 选项（页长）和 -w 选项（页宽）定义了宽65列，长15行的一个“页面”
