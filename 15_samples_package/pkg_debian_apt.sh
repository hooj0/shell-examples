# apt 将 apt-get 的常用命令集合在一个命令下，使用较为方便。在各种操作下还提供了比
# apt-get 更多可视化友好的功能。例如：提示升级软件包列表、查看可升级软件包、已经进度条
# ==========================================================================
# apt 1.2.32ubuntu0.1 (amd64)
# Usage: apt [options] command
#
# Most used commands:
#  list -       列出包名称
#  search -     搜索包
#  show -       显示包详细信息
#  install -    安装包
#  remove -     删除包
#  purge -      移除软件包及配置文件
#  autoremove - 自动删除所有未使用的包
#  update -     更新包列表
#  upgrade -    通过安装/升级包来升级系统
#  full-upgrade - 通过删除/安装/升级包来升级系统
#  edit-sources - 编辑源文件 source.list
# --------------------------------------------------------------------------



# ==========================================================================
# 示例：列出包名称
# ==========================================================================
sudo apt list

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：搜索包
# ==========================================================================
sudo apt search

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：显示包详细信息
# ==========================================================================
sudo apt show

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：安装包
# ==========================================================================
sudo apt install

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：删除包
# ==========================================================================
sudo apt remove

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：删除包和配置文件
# ==========================================================================
sudo apt purge

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：自动删除所有未使用的包
# ==========================================================================
sudo apt autoremove

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：更新包列表
# ==========================================================================
sudo apt update

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：通过安装/升级包来升级系统
# ==========================================================================
sudo apt upgrade

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：通过删除/安装/升级包来升级系统
# ==========================================================================
sudo apt full-upgrade

# output:
# --------------------------------------------------------------------------
#


# ==========================================================================
# 示例：编辑源文件 source.list
# ==========================================================================
sudo apt edit-sources

# output:
# --------------------------------------------------------------------------
#

read exits